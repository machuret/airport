def _climate_hazards(zone: str, snow: float, rain: int, city: str, non_hub: bool = False) -> list:
    hazards = []
    if zone == "northern_winter" and snow > 40:
        hazards.append(f"{city} receives significant annual snowfall — ice accumulation at outdoor boarding areas and curbside is a seasonal hazard from November through March")
    elif zone == "northern_winter":
        hazards.append(f"{city}'s winter weather creates periodic ice and snow conditions at all outdoor airport surfaces — a recurring slip and fall risk")
    elif zone == "tropical":
        hazards.append(f"{city}'s tropical climate means year-round high humidity and frequent rain — terminal entrances and curbside are persistently wet")
    elif zone == "coastal_rain":
        hazards.append(f"{city}'s coastal climate with {rain}+ rainy days annually creates persistent wet conditions at terminal entrances and outdoor areas")
    elif zone == "desert_dry":
        hazards.append(f"{city}'s desert climate creates extreme heat conditions in summer — outdoor boarding areas and curbside create heat emergency risk")
    elif zone == "mountain":
        hazards.append(f"{city}'s mountain location means significant snow and high altitude — both create unique injury and medical emergency risks")
    elif zone == "southern_humid":
        hazards.append(f"{city}'s humid climate with {rain}+ rainy days creates regular wet floor conditions and seasonal storm-related flooding at curbside areas")

    if non_hub:
        hazards.append(f"All aircraft boarding at this regional airport uses outdoor stairs — passengers are fully exposed to weather conditions during the boarding process")
        hazards.append("Regional airports typically have reduced cleaning and maintenance staff, increasing the time hazardous conditions remain unaddressed")
    else:
        hazards.append("Reduced staffing during early morning and late night operations means hazards take longer to be addressed at off-peak hours")

    return hazards

