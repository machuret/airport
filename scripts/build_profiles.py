# ── Build all profiles ────────────────────────────────────────────────────────

def build_all_profiles():
    with open(AIRPORTS_CSV) as f:
        airports = list(csv.DictReader(f))

    profiles = {}

    for airport in airports:
        iata = airport['iata_code'] or airport['faa_code']
        sc   = airport['state_code']
        climate = STATE_CLIMATE.get(sc, ("northern_winter", 20.0, 120))

        # Base profile
        if iata in LARGE_HUB_PROFILES:
            profile = LARGE_HUB_PROFILES[iata].copy()
        elif airport['type'] == 'large_hub':
            # Large hub not in our manual list — use a rich default
            profile = default_profile_medium(airport, climate)
            profile['annual_passengers_M'] = 8.0
            profile['passenger_tier'] = 'major'
            profile['daily_flights_approx'] = 300
            profile['gate_count_approx'] = 50
            profile['has_jet_bridges'] = True
        elif airport['type'] == 'medium_hub':
            profile = default_profile_medium(airport, climate)
        elif airport['type'] == 'small_hub':
            profile = default_profile_small(airport, climate)
        else:
            profile = default_profile_nonhub(airport, climate)

        # Always add these from lookup tables
        profile['climate_zone']        = climate[0]
        profile['avg_annual_snow_inches'] = climate[1]
        profile['avg_annual_rain_days']   = climate[2]
        profile['elevation_ft']           = ELEVATION.get(iata, 500)
        profile['nearby_courthouse']      = COURTHOUSE.get(sc, "U.S. District Court (district varies)")

        # Elevation risk flag
        if profile['elevation_ft'] > 4000:
            profile['high_altitude_medical_risk'] = True
        else:
            profile['high_altitude_medical_risk'] = False

        # PFAS upgrade for airports with known military history or fire training
        military_airports = {'ANC','FLL','HNL','MCO','TUS','ABQ','BDL','BHM','OKC','MCI','OMA','TLH','MSY'}
        if iata in military_airports:
            profile['pfas_risk'] = 'high'

        profiles[airport['slug']] = {
            "iata_code":    iata,
            "faa_code":     airport['faa_code'],
            "airport_name": airport['airport_name'],
            "city":         airport['city'],
            "state":        airport['state'],
            "state_code":   airport['state_code'],
            "type":         airport['type'],
            "slug":         airport['slug'],
            **profile
        }

    return profiles


